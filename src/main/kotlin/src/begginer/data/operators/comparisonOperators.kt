package src.begginer.data.operators


//Comparison operators(==, !=, <, > , <=,>=)

fun main() {

    val isEqual = 5 == 3
    println("Is $isEqual")
    val isNotEqual = 5 != 3

    //String Intepolation
    println("Is $isNotEqual")

    println("Is 5 greater than 3 : ${5 > 3}")
    println("Is 5 lower equal than 3 : ${5 <= 3}")
    println("Is 5 greater or equal  to 3 : ${5 >= 3}")

}